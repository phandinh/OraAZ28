-- Enable lai
BEGIN
  DBMS_FGA.enable_policy(
    object_schema   => 'HR',
    object_name     => 'EMPLOYEES',
    policy_name     => 'FGA_EMPLOYEES');
END;
/

-- Drop
BEGIN
  DBMS_FGA.drop_policy(
    object_schema   => 'HR',
    object_name     => 'EMPLOYEES',
    policy_name     => 'FGA_EMPLOYEES');
END;
